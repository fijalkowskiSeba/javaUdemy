public class Addition {
    private String name;
    private int price;

    public Addition(){
        this.name="null";
        this.price=0;
    }

    public Addition(String name, int price){
        this.name=name;
        this.price=price;
    }

    public String getName() {
        return name;
    }

    public int getPrice() {
        return price;
    }

}


